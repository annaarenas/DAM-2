/*Muestra la suma, resta,
multiplicación y división de dos
enteros largos prefijados, así como
el resto de dividir el primero entre el
segundo.*/

fun main() {
    val i = 1
    val j = 2
    println ("Suma: ${i + j}")
    println ("Resta: ${i - j}")
    println ("Multiplicación: ${i * j}")
    println ("División: ${i / j}")
    println ("Resto: ${i % j}")
}